public class positional {
    // Initialize public variables
    private int x;
    private int y;

    // Constructor that creates the positional object with inputs
    public positional(int x, int y) {
        this.x = x;
        this.y = y;
    }


    // Getter functions
    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }


    // Setter functions
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
